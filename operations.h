#ifndef OPERATIONS_H
#define OPERATIONS_H

int add(int num1, int num2)
{
  int answer;
  answer = num1 + num2;
  return answer;
}

int subtract(int num1, int num2)
{
  int answer;
  answer = num1 - num2;
  return answer;
}

int multiply(int num1, int num2)
{
  int answer;
  answer = num1 * num2;
  return answer;
}

int divide(int num1, int num2)
{
  int answer;
  answer = num1 / num2;
  return answer;
}
#endif
